"""
Utility functions module.
"""


def calculate_square(
    number: float
) -> float:
    """
    Calculate square.

    Args:
        number: Input number.

    Returns:
        float: Square value.
    """
    return number ** 2


def calculate_cube(
    number: float
) -> float:
    """
    Calculate cube.

    Args:
        number: Input number.

    Returns:
        float: Cube value.
    """
    return number ** 3
